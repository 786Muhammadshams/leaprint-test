        // Parse JSON.
        JAD::UniversalContainer container;
        try
        {
           // SUB e should be oe ligature
           boost::replace_all(json, "\032e", "&oelig;");

           // Look for characters that UC will fail to parse.
           for (int n = 0; n < json.size(); n++)
           {
              if ((unsigned char) json[n] < 32)
                 json[n] = ' ';
           }

            JAD::Buffer buf;
            buf.put_data(&json[0], (int) json.size());
            container = JAD::uc_decode_json(&buf);
        }
        catch (std::exception& e)
        {
            std::cout << "Exception thrown while decoding json: " << e.what() << std::endl;
            response.setStatus(Poco::Net::HTTPResponse::HTTP_INTERNAL_SERVER_ERROR);
            return;
        }
        catch (JAD::UniversalContainer& e)
        {
            std::cout << "Caught UC while decoding json: " << e["message"].str() << std::endl;
            response.setStatus(Poco::Net::HTTPResponse::HTTP_INTERNAL_SERVER_ERROR);
            return;
        }
        catch (...)
        {
            std::cout << "Unknown error thrown while decoding json.";
            response.setStatus(Poco::Net::HTTPResponse::HTTP_INTERNAL_SERVER_ERROR);
            return;
        }

        // Fill in RenderOptions.
        dr::RenderOptions opt;
        opt.interactive = false;
        opt.NarrowMargins();
        //opt.lang = lang == "de" ? 'D' : lang == "fr" ? 'F' : lang == "it" ? 'I' : 'D';
        opt.lang = 'D';

        //string cols = form.get("cols", "gray");
        string cols = "gray";
        opt.grayscale = cols == "gray";
        opt.night = cols == "night";
        opt.begin = ""; //form.get("begin", "");
        opt.end = ""; //form.get("end", "");

        string env = "P"; //form.get("env", "P");
        boost::to_upper(env);
        if (env == "P" || env == "I" || env == "T")
            opt.environment = env[0];
        else
            Logger::Warning(boost::format("DrivePrintHandler: invalid environment given: '%1%'") % env);

        std::string layout = "A4P"; //form.get("format", "A4L");
        if (layout == "A4L")
            opt.A4L();
        else if (layout == "A4P")
            opt.A4();
        else
        {
            opt.A4L();
            Logger::Warning(boost::format("DrivePrintHandler: unrecognized layout: %1%") % layout);
        }

        opt.tier = container["tier"].str();
        opt.renderColumns.push_back("notes");
        opt.renderColumns.push_back("km");
        opt.renderColumns.push_back("minus");
        opt.renderColumns.push_back("plus");
        opt.renderColumns.push_back("fchannel");
        opt.renderColumns.push_back("ae");
        opt.renderColumns.push_back("etcs");
        opt.renderColumns.push_back("main");
        opt.renderColumns.push_back("speed");
        opt.renderColumns.push_back("arrival");
        opt.renderColumns.push_back("departure");

        string additional = ""; //form.get("additional", "");
        if (!additional.empty())
           boost::split(opt.speeds, additional, boost::is_any_of(","));

        double ww = opt.w;
        double hh = opt.h;

        vector<uint8_t> buffer;
        buffer.reserve(128*1024*1024);

        cairo_surface_t* surface = cairo_pdf_surface_create_for_stream((cairo_write_func_t) bufwrite, (void*) &buffer, ww, hh);
        cairo_status_t ss = cairo_surface_status(surface);
        if (ss != CAIRO_STATUS_SUCCESS)
            Logger::Error(boost::format("Crror creating surface") % cairo_status_to_string(ss));

        cairo_t* g = cairo_create(surface);
        cairo_status_t cs = cairo_status(g);
        if (cs != CAIRO_STATUS_SUCCESS)
            Logger::Error(boost::format("Error creating context: %1%") % cairo_status_to_string(cs));

        // Create FO.
        Logger::Info("DrivePrintHandler: creating Fahrordnung");
        dr::Fahrordnung d(container);

        try
        {
            // Render FO.
            Logger::Info("DrivePrintHandler: rendering Fahrordnung");
            d.Render(g, opt);
            Logger::Info("DrivePrintHandler: successfully rendered Fahrordnung");
        }
        catch (std::exception& e)
        {
            Logger::Error(boost::format("DrivePrintHandler: render error: %1%") % e.what());
        }
        catch (JAD::UniversalContainer& e)
        {
            Logger::Error(boost::format("DrivePrintHandler: UC error"));
            JAD::Buffer* buf = uc_encode_ini(e);
            JAD::write_from_buffer(buf, std::cout);
            std:: cout << std::endl;
            delete buf;
        }
        catch (...)
        {
            Logger::Error("DrivePrintHandler: unknown error");
        }

        // Cleanup.
        cairo_destroy(g);
        cairo_surface_destroy(surface);

        Logger::Debug(boost::format("DrivePrintHandler: rendered %1% bytes.") % buffer.size());

        //// Send response.
        //if (form.get("protocol", "pdf") == "base64")
